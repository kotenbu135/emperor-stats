const READINGS: Map<string, string[]> = new Map(
  TABLE_SOURCE.trim()
    .split("\n")
    .map((line) => {
      const [char, readings] = line.split(":");
      return [char, readings.split(" ")] as const;
    }),
);

/** ひとつの名称文字列から生成する読み展開の上限（読み揺れの組合せ爆発の抑制）。 */
const MAX_EXPANSIONS = 8;

/**
 * 名称文字列をひらがな読みへ展開する。漢字は音読みテーブルの読み（複数あれば
 * 各組合せ・慣用読み優先で最大MAX_EXPANSIONS件）、カタカナ（モンゴル名等の別名）は
 * ひらがなへ変換、区切り記号（・、（）等）は読み飛ばす。
 * primaryOnlyのときは各文字の先頭読みだけで1件に展開する（王朝名・時代ラベル用）。
 */
export function kanaExpansionsOf(
  text: string,
  { primaryOnly = false }: { primaryOnly?: boolean } = {},
): string[] {
  let expansions = [""];
  for (const char of text) {
    let readings: string[];
    if (READINGS.has(char)) {
      readings = READINGS.get(char)!;
    } else if (/[ァ-ヶーぁ-ゖ]/.test(char)) {
      readings = [toHiragana(char)];
    } else if (/\p{Script=Han}/u.test(char)) {
      throw new Error(
        `かな検索テーブルに未登録の漢字です: "${char}"（kana-readings.tsのTABLE_SOURCEに読みを追加してください）`,
      );
    } else {
      continue; // 区切り記号（・、（）/〜など）は読みに含めない
    }
    if (primaryOnly) readings = readings.slice(0, 1);
    expansions = expansions
      .flatMap((prefix) => readings.map((r) => prefix + r))
      .slice(0, MAX_EXPANSIONS);
  }
  return expansions.filter(Boolean);
}
